import java.util.*;
class Ifelsecondition {
    public static void main(String[] args) {
        Scanner Scan = new Scanner(System.in);
        System.out.println("Enter the value a");
        int a = Scan.nextInt();
        if (a<=100){
            System.out.println("a is inside 100");
        }
        else {
            System.out.println("a is not inside 100");
        }
    }
}

