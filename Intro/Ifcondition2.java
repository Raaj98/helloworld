class Ifcondition2{
    public static void main(String[]args){
        int a = 200;
        if(a>=500){
            System.out.println("a is not inside 500 ");
        }
        else if (a<=500){
            System.out.println("a is inside 500");
        }
        else if (a==500){
            System.out.println("a is not equal to 500");
        }
        else if (a!=500){
            System.out.println("this condition is false");
        }
    }
}